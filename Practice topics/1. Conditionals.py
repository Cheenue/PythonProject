# Traffic signs

traffic_light = input("What is the traffic light color? ")

if traffic_light == "green":
    print("You are good to go forward!")
elif traffic_light == "yellow":
    print("Slow down! It's going to turn red soon!")
elif (traffic_light == "red"):
    print("Stop dumbass! It's red!")
else:
    print("Sorry, traffic light color is not recognized")


